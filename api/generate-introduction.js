export default async function handler(request, response) {
  // Log that the function is being called
  console.log('üîç /api/generate-introduction called');
  console.log('üîç Method:', request.method);
  console.log('üîç Headers:', Object.fromEntries(request.headers));
  
  // Always set CORS headers first
  response.setHeader('Access-Control-Allow-Origin', '*');
  response.setHeader('Access-Control-Allow-Methods', 'GET, POST, PUT, DELETE, OPTIONS');
  response.setHeader('Access-Control-Allow-Headers', 'Content-Type, Authorization');
  response.setHeader('Access-Control-Max-Age', '86400'); // 24 hours
  
  // Handle preflight requests
  if (request.method === 'OPTIONS') {
    console.log('üîç OPTIONS preflight request handled');
    response.status(200).end();
    return;
  }
  
  // Accept POST requests
  if (request.method !== 'POST') {
    console.log('‚ùå Method not allowed:', request.method);
    return response.status(405).json({ 
      error: 'Method not allowed',
      allowed: ['POST'],
      received: request.method,
      message: 'This endpoint only accepts POST requests'
    });
  }

  try {
    console.log('üì• Processing POST request');
    
    // Check if we can read the body
    let jsonData;
    try {
      jsonData = await request.json();
      console.log('üì• Parsed JSON data successfully');
    } catch (parseError) {
      console.error('‚ùå Failed to parse JSON:', parseError);
      return response.status(400).json({ 
        error: 'Invalid JSON in request body',
        details: parseError.message
      });
    }
    
    const { prompt } = jsonData || {};
    console.log('üì• Received prompt, exists:', !!prompt);

    if (!prompt) {
      console.log('‚ùå Prompt is required');
      return response.status(400).json({ 
        error: 'Prompt is required',
        received: jsonData
      });
    }

    // Get OpenAI API key from environment variables
    const openaiApiKey = process.env.VITE_OPENAI_API_KEY;
    
    if (!openaiApiKey) {
      console.error('‚ùå Missing OpenAI API key in environment variables');
      // Return a mock response for testing
      console.log('üîß Returning mock response for testing');
      return response.status(200).json({
        success: true,
        introduction: "This is a mock introduction generated for testing purposes. In production, this would be generated by OpenAI based on your job details and resume."
      });
    }

    console.log('üîë OpenAI API key found, calling OpenAI API');
    
    // Call OpenAI API
    const openaiResponse = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openaiApiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          {
            role: 'system',
            content: 'You are a professional career coach specializing in creating compelling self-introductions for job applicants.'
          },
          {
            role: 'user',
            content: prompt
          }
        ],
        max_tokens: 350,
        temperature: 0.7,
      }),
    });

    const data = await openaiResponse.json();
    console.log('üì§ OpenAI API response status:', openaiResponse.status);
    
    if (!openaiResponse.ok) {
      console.error('‚ùå OpenAI API Error:', data);
      return response.status(500).json({ 
        error: 'Failed to generate introduction',
        openaiError: data.error ? data.error.message : 'Unknown OpenAI error',
        status: openaiResponse.status
      });
    }

    if (data.choices && data.choices[0]) {
      console.log('‚úÖ Successfully generated introduction');
      return response.status(200).json({
        success: true,
        introduction: data.choices[0].message.content
      });
    } else {
      console.error('‚ùå No response from OpenAI');
      return response.status(500).json({ 
        error: 'No response from OpenAI',
        details: data
      });
    }
    
  } catch (error) {
    console.error('üí• API Error:', error);
    return response.status(500).json({
      success: false,
      error: 'Failed to generate introduction',
      details: error.message,
      stack: process.env.NODE_ENV === 'development' ? error.stack : undefined
    });
  }
}